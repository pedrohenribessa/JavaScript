// Type conversion (typecasting) vs Type coersion 

/*
    # Falsy
    - Quando um valor é conciderado false em contextos onde um booleano é obrigatório (condicionais e loops)

    false
    0
    -0
    ""
    null
    undefined
    NaN
*/

console.log( 0 ? 'vedadeiro' : 'falso')

/*
    # Truthy
    - Quando um valor é conciderado true em contextos onde um booleano é obrigatório (condicionais e loops)

    true
    {}
    []
    1
    3.23
    "0"
    "false"
    -1
    Infiniyt
    -Infiniyt
*/

console.log( true ? 'vedadeiro' : 'falso')
