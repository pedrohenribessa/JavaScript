// Operadores de comparação

// Irá comparar valores e retomar um Boolean como resposta a comparação

let one = 1
let two = 2

// ==        igual a
console.log( two == 1 )
console.log( one == "1")

// !=       diferente de  
console.log( one != two)
console.log( one != 1)
console.log( one != "1")

// Irá comparar valores estritamente igual a e estritamente diferente de

// ===      estritamente igual a
console.log( one === "1")
console.log( one === 1)

// !==      estritamente diferente de
console.log( two !== "2")
console.log( two !== 2)

// Irá comparar valores maior menor e igual

// >        Maior que]
console.log(one > two)

// >=        Maior e igual a
console.log(one >= 1)
console.log(two >= 1)

// <        Menor que
console.log(one < two)

// <=       Menor ou igual a
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
